package thevent_test

import (
	"context"
	"fmt"
	"testing"
)

import (
	"github.com/dhui/thevent"
)

func setupEvent(b *testing.B, n int) *thevent.Event {
	e, err := thevent.New(5, handlers[0:n]...)
	if err != nil {
		b.Fatal("Unable to create event:", err)
	}
	return e
}

func BenchmarkEvents(b *testing.B) {
	for numHandlers := 4; numHandlers <= 4096; numHandlers *= 2 {
		b.Run(fmt.Sprint(numHandlers, "handlers"), func(b *testing.B) {
			ctx := context.Background()
			e := setupEvent(b, numHandlers)
			b.Run("Dispatch", func(b *testing.B) {
				for i := 0; i < b.N; i++ {
					if err := e.Dispatch(ctx, i); err != nil {
						b.Error("Error dispatching:", err)
					}
				}
			})

			e = setupEvent(b, numHandlers)
			b.Run("DispatchWithResults", func(b *testing.B) {
				for i := 0; i < b.N; i++ {
					if _, err := e.DispatchWithResults(ctx, i); err != nil {
						b.Error("Error dispatching:", err)
					}
				}
			})

			e = setupEvent(b, numHandlers)
			b.Run("DispatchAsync", func(b *testing.B) {
				for i := 0; i < b.N; i++ {
					if err := e.DispatchAsync(ctx, i); err != nil {
						b.Error("Error dispatching:", err)
					}
				}
			})

			e = setupEvent(b, numHandlers)
			b.Run("DispatchAsyncWithErrors", func(b *testing.B) {
				for i := 0; i < b.N; i++ {
					ch, err := e.DispatchAsyncWithErrors(ctx, i)
					// Don't "leak" the goroutines blocked by the unbuffered channel
					go func() {
						for range ch {
						}
					}()
					if err != nil {
						b.Error("Error dispatching:", err)
					}
				}
			})
		})
	}
}

